package technic.race;

public class BonusAjax {

	private String idBonus;
	private String valBonus;
	private boolean acadBonus;
	
	
	public BonusAjax(){
		
	}


	public String getIdBonus() {
		return idBonus;
	}


	public void setIdBonus(String idBonus) {
		this.idBonus = idBonus;
	}


	public String getValBonus() {
		return valBonus;
	}


	public void setValBonus(String valBonus) {
		this.valBonus = valBonus;
	}


	public boolean isAcadBonus() {
		return acadBonus;
	}


	public void setAcadBonus(boolean acadBonus) {
		this.acadBonus = acadBonus;
	}



}
